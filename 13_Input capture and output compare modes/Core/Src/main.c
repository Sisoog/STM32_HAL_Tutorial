/*********************  TIM2 (Input Capture)  ********************/
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef* htim)
{
    if(captureState == AwaitingCapture)
    {
    	TIM2_overFlowCnt = 0;
	    T1 = TIM2->CCR1;
        captureState = FirstCapture;
    }
    else if(captureState == FirstCapture)
    {
    	T2 = TIM2->CCR1;
        captureState = SecondCapture;
        __HAL_TIM_SetCounter(&htim2, 0);
    }
}

